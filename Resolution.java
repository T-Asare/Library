public class Resolution {
    private int height;
    private int length;
    private int depth;

    public Resolution(int height, int length, int depth) {
        this.height = height;
        this.length = length;
        this.depth = depth;
    }

    public int getHeight() {
        return height;
    }

    public int getLength() {
        return length;
    }

    public int getDepth() {
        return depth;
    }
}
